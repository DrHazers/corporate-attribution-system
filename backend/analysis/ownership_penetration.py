def _build_canonical_path_edges_from_factors(
    path_state: PathState,
) -> list[dict]:
    edges: list[dict] = []
    for factor in path_state.edge_factors:
        edge = {
            "structure_id": factor.structure_id,
            "from_entity_id": factor.from_entity_id,
            "to_entity_id": factor.to_entity_id,
            "relation_type": factor.relation_type,
            "relation_role": factor.relation_role,
            "numeric_factor": serialize_unit_score(factor.numeric_factor),
            "semantic_factor": serialize_unit_score(factor.semantic_factor),
            "confidence_weight": serialize_unit_score(factor.confidence_weight),
            "reliability_score": serialize_unit_score(factor.reliability_score),
            "reliability_flags": list(factor.reliability_flags) or None,
            "confidence_weight_source": factor.evidence.get("confidence_weight_source"),
            "flags": list(factor.flags),
            "evidence_summary": factor.evidence.get("evidence_summary"),
        }
        reliability_breakdown = factor.evidence.get("reliability_breakdown")
        if reliability_breakdown is not None:
            edge["reliability_breakdown"] = reliability_breakdown
        evidence_breakdown = factor.evidence.get("evidence_breakdown")
        if evidence_breakdown is not None:
            edge["evidence_breakdown"] = evidence_breakdown
        edges.append(edge)
    return edges


def _serialize_unified_path(
    path_state: PathState,
    *,
    context: ControlInferenceContext,
) -> dict:
    path_score = path_state.numeric_prod * path_state.semantic_prod
    return {
        "path_entity_ids": path_state.entity_ids,
        "path_entity_names": [
            context.entity_map[entity_id].entity_name
            for entity_id in path_state.entity_ids
            if entity_id in context.entity_map
        ],
        "edge_ids": path_state.edge_ids,
        "edges": _build_canonical_path_edges_from_factors(path_state),
        "numeric_prod": serialize_unit_score(path_state.numeric_prod),
        "semantic_prod": serialize_unit_score(path_state.semantic_prod),
        "confidence_prod": serialize_unit_score(path_state.conf_prod),
        "path_score": serialize_unit_score(path_score),
        "path_score_pct": serialize_pct_score(path_score),
        "semantic_flags": _semantic_flags_for_storage(path_state.flags),
    }


def _serialize_unified_paths(
    path_states: tuple[PathState, ...],
    *,
    context: ControlInferenceContext,
) -> str:
    payload = [
        _serialize_unified_path(path_state, context=context)
        for path_state in path_states
    ]
    return json.dumps(payload, ensure_ascii=False, sort_keys=True)


