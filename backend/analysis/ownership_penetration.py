def refresh_company_control_analysis(
    db: Session,
    company_id: int,
    as_of: date | datetime | None = None,
    max_depth: int = DEFAULT_MAX_DEPTH,
    min_path_ratio_pct: Decimal = DEFAULT_MIN_PATH_RATIO_PCT,
    majority_threshold_pct: Decimal = DEFAULT_MAJORITY_THRESHOLD_PCT,
    disclosure_threshold_pct: Decimal = DEFAULT_DISCLOSURE_THRESHOLD_PCT,
) -> dict:
    if max_depth < 1:
        raise ValueError("max_depth must be at least 1.")
    if min_path_ratio_pct < ZERO:
        raise ValueError("min_path_ratio_pct must be non-negative.")
    if _use_unified_control_engine():
        unified_context = build_control_context(db, as_of)
        try:
            result = _refresh_company_control_analysis_with_unified_context(
                db,
                company_id,
                context=unified_context,
                max_depth=max_depth,
                min_path_ratio_pct=min_path_ratio_pct,
                majority_threshold_pct=majority_threshold_pct,
                disclosure_threshold_pct=disclosure_threshold_pct,
            )
            db.commit()
            return result
        except Exception:
            db.rollback()
            raise

    legacy_context = build_ownership_analysis_context(db, as_of)

    try:
        result = _refresh_company_control_analysis_with_context(
            db,
            company_id,
            context=legacy_context,
            max_depth=max_depth,
            min_path_ratio_pct=min_path_ratio_pct,
            majority_threshold_pct=majority_threshold_pct,
            disclosure_threshold_pct=disclosure_threshold_pct,
        )
        db.commit()
        return result
    except Exception:
        db.rollback()
        raise


def refresh_all_companies_control_analysis(
    db: Session,
    batch_size: int = 100,
) -> dict:
    if batch_size < 1:
        raise ValueError("batch_size must be at least 1.")

    started_at = perf_counter()
    use_unified_engine = _use_unified_control_engine()
    context = (
        build_control_context(db)
        if use_unified_engine
        else build_ownership_analysis_context(db)
    )
    company_ids = sorted(context.company_map)
    total = len(company_ids)
    success_count = 0
    failed_count = 0
    per_company_durations: list[float] = []

    print("[START] control analysis")

    for batch_start in range(0, total, batch_size):
        batch_end = min(batch_start + batch_size, total)
        batch_company_ids = company_ids[batch_start:batch_end]
        batch_success_count = 0

        print(f"[BATCH] processing {batch_start + 1}-{batch_end} / {total}")

        for company_id in batch_company_ids:
            company_started_at = perf_counter()
            try:
                with db.begin_nested():
                    if use_unified_engine:
                        result = _refresh_company_control_analysis_with_unified_context(
                            db,
                            company_id,
                            context=context,
                            max_depth=DEFAULT_MAX_DEPTH,
                            min_path_ratio_pct=DEFAULT_MIN_PATH_RATIO_PCT,
                            majority_threshold_pct=DEFAULT_MAJORITY_THRESHOLD_PCT,
                            disclosure_threshold_pct=DEFAULT_DISCLOSURE_THRESHOLD_PCT,
                        )
                    else:
                        result = _refresh_company_control_analysis_with_context(
                            db,
                            company_id,
                            context=context,
                            max_depth=DEFAULT_MAX_DEPTH,
                            min_path_ratio_pct=DEFAULT_MIN_PATH_RATIO_PCT,
                            majority_threshold_pct=DEFAULT_MAJORITY_THRESHOLD_PCT,
                            disclosure_threshold_pct=DEFAULT_DISCLOSURE_THRESHOLD_PCT,
                        )
                success_count += 1
                batch_success_count += 1
                print(
                    f"[COMPANY] {company_id} done, "
                    f"controllers={result['control_relationship_count']}"
                )
            except Exception as exc:
                failed_count += 1
                db.expire_all()
                print(f"[COMPANY] {company_id} failed: {exc}")
            finally:
                per_company_durations.append(perf_counter() - company_started_at)

        try:
            db.commit()
            print(f"[BATCH] committed {len(batch_company_ids)}")
        except Exception as exc:
            db.rollback()
            success_count -= batch_success_count
            failed_count += batch_success_count
            print(
                f"[BATCH] commit failed {batch_start + 1}-{batch_end} / {total}: {exc}"
            )

    total_duration_seconds = perf_counter() - started_at
    average_duration_seconds = (
        sum(per_company_durations) / len(per_company_durations)
        if per_company_durations
        else 0.0
    )

    print(f"[END] total processed = {total}")

    return {
        "total_processed": total,
        "success_count": success_count,
        "failed_count": failed_count,
        "average_duration_seconds": average_duration_seconds,
        "total_duration_seconds": total_duration_seconds,
    }


def _normalize_path_edges(
    *,
    path_item: dict,
    relation_type_fallback: str,
) -> list[dict]:
    path_entity_ids = path_item.get("path_entity_ids") or []
    raw_edges = path_item.get("edges")
    if isinstance(raw_edges, list) and raw_edges:
        normalized_edges = []
        for index, edge in enumerate(raw_edges):
            normalized_edges.append(
                {
                    "structure_id": edge.get("structure_id"),
                    "from_entity_id": edge.get("from_entity_id")
                    if edge.get("from_entity_id") is not None
                    else (
                        path_entity_ids[index]
                        if index < len(path_entity_ids)
                        else None
                    ),
                    "to_entity_id": edge.get("to_entity_id")
                    if edge.get("to_entity_id") is not None
                    else (
                        path_entity_ids[index + 1]
                        if index + 1 < len(path_entity_ids)
                        else None
                    ),
                    "relation_type": edge.get("relation_type") or relation_type_fallback,
                    "relation_role": edge.get("relation_role"),
                    "numeric_factor": _serialize_probability(edge.get("numeric_factor")),
                    "semantic_factor": _serialize_probability(
                        edge.get("semantic_factor")
                    )
                    or "1.0000",
                    "confidence_weight": _serialize_probability(
                        edge.get("confidence_weight")
                    )
                    or "1.0000",
                    "reliability_score": _serialize_probability(
                        edge.get("reliability_score")
                    )
                    or _serialize_probability(edge.get("confidence_weight"))
                    or "1.0000",
                    "reliability_flags": _parse_json_list(
                        edge.get("reliability_flags")
                    ),
                    "confidence_weight_source": edge.get("confidence_weight_source"),
                    "reliability_breakdown": edge.get("reliability_breakdown"),
                    "evidence_breakdown": edge.get("evidence_breakdown"),
                    "flags": _parse_json_list(edge.get("flags")) or [],
                    "evidence_summary": edge.get("evidence_summary"),
                }
            )
        return normalized_edges

    raw_edge_factors = path_item.get("edge_factors")
    if isinstance(raw_edge_factors, list) and raw_edge_factors:
        normalized_edges = []
        for index, edge in enumerate(raw_edge_factors):
            normalized_edges.append(
                {
                    "structure_id": edge.get("structure_id"),
                    "from_entity_id": (
                        path_entity_ids[index]
                        if index < len(path_entity_ids)
                        else None
                    ),
                    "to_entity_id": (
                        path_entity_ids[index + 1]
                        if index + 1 < len(path_entity_ids)
                        else None
                    ),
                    "relation_type": edge.get("relation_type") or relation_type_fallback,
                    "relation_role": edge.get("relation_role"),
                    "numeric_factor": _serialize_probability(edge.get("numeric_factor")),
                    "semantic_factor": _serialize_probability(
                        edge.get("semantic_factor")
                    )
                    or "1.0000",
                    "confidence_weight": _serialize_probability(
                        edge.get("confidence_weight")
                    )
                    or "1.0000",
                    "reliability_score": _serialize_probability(
                        edge.get("reliability_score")
                    )
                    or _serialize_probability(edge.get("confidence_weight"))
                    or "1.0000",
                    "reliability_flags": _parse_json_list(
                        edge.get("reliability_flags")
                    ),
                    "confidence_weight_source": edge.get("confidence_weight_source"),
                    "reliability_breakdown": edge.get("reliability_breakdown"),
                    "evidence_breakdown": edge.get("evidence_breakdown"),
                    "flags": _parse_json_list(edge.get("flags")) or [],
                    "evidence_summary": edge.get("evidence_summary"),
                }
            )
        return normalized_edges

    raw_edge_ratios = path_item.get("edge_holding_ratio_pct") or []
    if isinstance(raw_edge_ratios, list) and raw_edge_ratios:
        return [
            {
                "structure_id": None,
                "from_entity_id": (
                    path_entity_ids[index] if index < len(path_entity_ids) else None
                ),
                "to_entity_id": (
                    path_entity_ids[index + 1]
                    if index + 1 < len(path_entity_ids)
                    else None
                ),
                "relation_type": relation_type_fallback,
                "relation_role": "ownership"
                if relation_type_fallback == "equity"
                else None,
                "numeric_factor": _serialize_probability(ratio),
                "semantic_factor": "1.0000",
                "confidence_weight": "1.0000",
                "reliability_score": "1.0000",
                "reliability_flags": None,
                "confidence_weight_source": None,
                "reliability_breakdown": None,
                "evidence_breakdown": None,
                "flags": [relation_type_fallback] if relation_type_fallback else [],
                "evidence_summary": None,
            }
            for index, ratio in enumerate(raw_edge_ratios)
        ]

    return []


def _normalize_control_path_payload(
    payload: list | dict | str | None,
    *,
    control_mode: str | None,
    semantic_flags: list[str] | None,
) -> list | dict | str | None:
    if isinstance(payload, str):
        payload = _deserialize_json(payload)
    if not isinstance(payload, list):
        return payload

    relation_type_fallback = (
        "equity"
        if control_mode in {None, "numeric"}
        else (
            "board_control"
            if semantic_flags and "board_control" in semantic_flags
            else "agreement"
        )
    )
    normalized_paths = []
    for path_item in payload:
        if not isinstance(path_item, dict):
            normalized_paths.append(path_item)
            continue

        item_relation_type_fallback = relation_type_fallback
        if isinstance(path_item.get("edge_holding_ratio_pct"), list):
            item_relation_type_fallback = "equity"

        normalized_edges = _normalize_path_edges(
            path_item=path_item,
            relation_type_fallback=item_relation_type_fallback,
        )
        path_entity_ids = path_item.get("path_entity_ids") or []
        path_score = (
            _serialize_probability(path_item.get("path_score"))
            or _serialize_probability(path_item.get("path_score_pct"))
            or _serialize_probability(path_item.get("path_ratio_pct"))
            or "0.0000"
        )
        normalized_paths.append(
            {
                "path_entity_ids": path_entity_ids,
                "path_entity_names": path_item.get("path_entity_names") or [],
                "edge_ids": path_item.get("edge_ids")
                or [edge.get("structure_id") for edge in normalized_edges],
                "edges": normalized_edges,
                "numeric_prod": _serialize_probability(path_item.get("numeric_prod"))
                or (
                    path_score
                    if control_mode == "numeric"
                    else "1.0000"
                ),
                "semantic_prod": _serialize_probability(path_item.get("semantic_prod"))
                or (
                    "1.0000"
                    if control_mode == "numeric"
                    else path_score
                ),
                "confidence_prod": _serialize_probability(
                    path_item.get("confidence_prod")
                )
                or "1.0000",
                "path_score": path_score,
                "path_score_pct": (
                    path_item.get("path_score_pct")
                    or path_item.get("path_ratio_pct")
                    or _serialize_pct_from_any(path_score)
                ),
                "semantic_flags": _parse_json_list(
                    path_item.get("semantic_flags")
                )
                or semantic_flags,
            }
        )

    return normalized_paths


def _normalize_basis_payload(
    payload: dict | str | None,
    *,
    classification: str | None,
    control_mode: str | None,
    semantic_flags: list[str] | None,
    control_path: list | dict | str | None,
    control_ratio: str | None,
) -> dict | str | None:
    parsed = payload
    if isinstance(payload, str):
        parsed = _deserialize_json(payload)
    if not isinstance(parsed, dict):
        return parsed

    total_score = (
        parsed.get("total_score")
        or _serialize_probability(parsed.get("total_score_pct"))
        or _serialize_probability(parsed.get("total_ratio_pct"))
        or _serialize_probability(control_ratio)
        or "0.0000"
    )
    normalized_paths = _normalize_control_path_payload(
        parsed.get("top_paths") or control_path,
        control_mode=parsed.get("control_mode") or control_mode,
        semantic_flags=_parse_json_list(parsed.get("semantic_flags")) or semantic_flags,
    )

    return {
        **parsed,
        "aggregator": parsed.get("aggregator") or "sum_cap",
        "classification": _canonical_control_type(
            parsed.get("classification") or classification
        )
        or classification,
        "control_mode": parsed.get("control_mode") or control_mode,
        "semantic_flags": _parse_json_list(parsed.get("semantic_flags")) or semantic_flags,
        "top_paths": normalized_paths if isinstance(normalized_paths, list) else [],
        "evidence_summary": parsed.get("evidence_summary") or [],
        "total_confidence": _serialize_probability(parsed.get("total_confidence"))
        or "1.0000",
        "total_score": total_score,
        "total_score_pct": parsed.get("total_score_pct")
        or _serialize_pct_from_any(total_score),
    }


def _normalize_country_basis_payload(
    payload: dict | str | None,
    *,
    attribution_type: str | None,
) -> dict | str | None:
    parsed = payload
    if isinstance(payload, str):
        parsed = _deserialize_json(payload)
    if not isinstance(parsed, dict):
        return parsed

    total_score = (
        parsed.get("total_score")
        or _serialize_probability(parsed.get("total_score_pct"))
        or "0.0000"
    )
    top_paths = _normalize_control_path_payload(
        parsed.get("top_paths"),
        control_mode=parsed.get("control_mode"),
        semantic_flags=_parse_json_list(parsed.get("semantic_flags")),
    )
    def _normalize_country_candidate_item(item: dict | None) -> dict | None:
        if not isinstance(item, dict):
            return None
        return {
            **item,
            "classification": _canonical_control_type(item.get("classification"))
            or item.get("classification"),
            "control_type": _canonical_control_type(
                item.get("control_type") or item.get("classification")
            )
            or item.get("control_type")
            or item.get("classification"),
            "semantic_flags": _parse_json_list(item.get("semantic_flags")),
            "total_confidence": _serialize_probability(item.get("total_confidence"))
            or "0.0000",
            "total_score": _serialize_probability(item.get("total_score"))
            or _serialize_probability(item.get("total_score_pct"))
            or "0.0000",
            "total_score_pct": item.get("total_score_pct")
            or _serialize_pct_from_any(item.get("total_score")),
            "control_path": _normalize_control_path_payload(
                item.get("control_path"),
                control_mode=item.get("control_mode"),
                semantic_flags=_parse_json_list(item.get("semantic_flags")),
            )
            or [],
            "whether_actual_controller": bool(
                item.get("whether_actual_controller")
                if item.get("whether_actual_controller") is not None
                else item.get("is_actual_controller")
            ),
            "is_leading_candidate": bool(item.get("is_leading_candidate")),
            "control_tier": item.get("control_tier"),
            "is_direct_controller": bool(item.get("is_direct_controller")),
            "is_intermediate_controller": bool(item.get("is_intermediate_controller")),
            "is_ultimate_controller": bool(item.get("is_ultimate_controller")),
            "promotion_source_entity_id": item.get("promotion_source_entity_id"),
            "promotion_reason": item.get("promotion_reason"),
            "control_chain_depth": item.get("control_chain_depth"),
            "is_terminal_inference": bool(item.get("is_terminal_inference")),
            "terminal_failure_reason": item.get("terminal_failure_reason"),
            "terminal_identifiability": item.get("terminal_identifiability"),
            "terminal_suitability": item.get("terminal_suitability"),
            "terminal_profile_reasons": item.get("terminal_profile_reasons") or [],
            "ownership_pattern_signal": bool(item.get("ownership_pattern_signal")),
            "immediate_control_ratio": item.get("immediate_control_ratio"),
            "aggregated_control_score": _serialize_probability(
                item.get("aggregated_control_score")
            )
            or _serialize_probability(item.get("total_score"))
            or _serialize_probability(item.get("total_score_pct")),
            "terminal_control_score": _serialize_probability(
                item.get("terminal_control_score")
            ),
        }

    top_candidates = parsed.get("top_candidates")
    normalized_top_candidates = []
    if isinstance(top_candidates, list):
        for item in top_candidates:
            normalized_item = _normalize_country_candidate_item(item)
            if normalized_item is not None:
                normalized_top_candidates.append(normalized_item)
    normalized_leading_candidate = _normalize_country_candidate_item(
        parsed.get("leading_candidate")
    )
    normalized_direct_controller = _normalize_country_candidate_item(
        parsed.get("direct_controller")
    )

    return {
        **parsed,
        "classification": _canonical_attribution_type(
            parsed.get("classification") or attribution_type
        )
        or attribution_type,
        "attribution_type": _canonical_attribution_type(
            parsed.get("attribution_type") or attribution_type
        )
        or attribution_type,
        "semantic_flags": _parse_json_list(parsed.get("semantic_flags")),
        "top_paths": top_paths if isinstance(top_paths, list) else [],
        "evidence_summary": parsed.get("evidence_summary") or [],
        "total_confidence": _serialize_probability(parsed.get("total_confidence"))
        or "0.0000",
        "total_score": total_score,
        "total_score_pct": parsed.get("total_score_pct")
        or _serialize_pct_from_any(total_score),
        "controller_status": parsed.get("controller_status"),
        "actual_controller_entity_id": parsed.get("actual_controller_entity_id"),
        "direct_controller_entity_id": parsed.get("direct_controller_entity_id"),
        "attribution_layer": parsed.get("attribution_layer"),
        "country_inference_reason": parsed.get("country_inference_reason"),
        "look_through_applied": bool(parsed.get("look_through_applied", False)),
        "promotion_path_entity_ids": parsed.get("promotion_path_entity_ids") or [],
        "promotion_source_by_entity_id": parsed.get("promotion_source_by_entity_id")
        or {},
        "promotion_reason_by_entity_id": parsed.get("promotion_reason_by_entity_id")
        or {},
        "terminal_failure_reason": parsed.get("terminal_failure_reason"),
        "leading_candidate_entity_id": parsed.get("leading_candidate_entity_id"),
        "leading_candidate_classification": parsed.get(
            "leading_candidate_classification"
        ),
        "leading_candidate": normalized_leading_candidate,
        "direct_controller": normalized_direct_controller,
        "top_candidates": normalized_top_candidates,
    }


def _serialize_control_relationship_response(
    relationship: ControlRelationship,
) -> dict:
    semantic_flags = _parse_json_list(relationship.semantic_flags)
    control_mode = relationship.control_mode
    control_type = _canonical_control_type(relationship.control_type)
    control_ratio = _serialize_ratio(relationship.control_ratio)
    control_path = _normalize_control_path_payload(
        relationship.control_path,
        control_mode=control_mode,
        semantic_flags=semantic_flags,
    )
    normalized_basis = _normalize_basis_payload(
        relationship.basis,
        classification=control_type,
        control_mode=control_mode,
        semantic_flags=semantic_flags,
        control_path=control_path,
        control_ratio=control_ratio,
    )
    basis_controller_status = (
        normalized_basis.get("controller_status")
        if isinstance(normalized_basis, dict)
        else None
    )
    basis_selection_reason = (
        normalized_basis.get("selection_reason")
        if isinstance(normalized_basis, dict)
        else None
    )
    basis_is_leading = (
        bool(normalized_basis.get("is_leading_candidate"))
        if isinstance(normalized_basis, dict)
        else False
    )
    return {
        "id": relationship.id,
        "company_id": relationship.company_id,
        "controller_entity_id": relationship.controller_entity_id,
        "controller_name": relationship.controller_name,
        "controller_type": relationship.controller_type,
        "control_type": control_type,
        "control_ratio": control_ratio,
        "control_path": control_path,
        "is_actual_controller": relationship.is_actual_controller,
        "whether_actual_controller": relationship.is_actual_controller,
        "control_tier": relationship.control_tier,
        "is_direct_controller": bool(relationship.is_direct_controller),
        "is_intermediate_controller": bool(relationship.is_intermediate_controller),
        "is_ultimate_controller": bool(relationship.is_ultimate_controller),
        "promotion_source_entity_id": relationship.promotion_source_entity_id,
        "promotion_reason": relationship.promotion_reason,
        "control_chain_depth": relationship.control_chain_depth,
        "is_terminal_inference": bool(relationship.is_terminal_inference),
        "terminal_failure_reason": relationship.terminal_failure_reason,
        "immediate_control_ratio": _serialize_ratio(
            relationship.immediate_control_ratio
        ),
        "aggregated_control_score": _serialize_probability(
            relationship.aggregated_control_score
        ),
        "terminal_control_score": _serialize_probability(
            relationship.terminal_control_score
        ),
        "inference_run_id": relationship.inference_run_id,
        "basis": normalized_basis,
        "notes": relationship.notes,
        "control_mode": control_mode,
        "semantic_flags": semantic_flags,
        "controller_status": basis_controller_status,
        "selection_reason": basis_selection_reason,
        "is_leading_candidate": basis_is_leading,
        "terminal_identifiability": (
            normalized_basis.get("terminal_identifiability")
            if isinstance(normalized_basis, dict)
            else None
        ),
        "terminal_suitability": (
            normalized_basis.get("terminal_suitability")
            if isinstance(normalized_basis, dict)
            else None
        ),
        "terminal_profile_reasons": (
            normalized_basis.get("terminal_profile_reasons") or []
            if isinstance(normalized_basis, dict)
            else []
        ),
        "ownership_pattern_signal": (
            bool(normalized_basis.get("ownership_pattern_signal"))
            if isinstance(normalized_basis, dict)
            else False
        ),
        "review_status": relationship.review_status,
        "created_at": relationship.created_at.isoformat(),
        "updated_at": relationship.updated_at.isoformat(),
    }


def get_company_control_chain_data(db: Session, company_id: int) -> dict:
    relationships = (
        db.query(ControlRelationship)
        .filter(ControlRelationship.company_id == company_id)
        .filter(
            or_(
                ControlRelationship.notes.is_(None),
                ~ControlRelationship.notes.like("MANUAL_OVERRIDE:%"),
            )
        )
        .order_by(
            ControlRelationship.control_ratio.is_(None),
            ControlRelationship.control_ratio.desc(),
            ControlRelationship.id.asc(),
        )
        .all()
    )
    serialized_relationships = [
        _serialize_control_relationship_response(relationship)
        for relationship in relationships
    ]

    actual_controller = next(
        (
            relationship
            for relationship in serialized_relationships
            if relationship["is_ultimate_controller"]
            or relationship["is_actual_controller"]
        ),
        None,
    )
    direct_controller = next(
        (
            relationship
            for relationship in serialized_relationships
            if relationship.get("is_direct_controller")
        ),
        None,
    )
    leading_candidate = actual_controller or next(
        (
            relationship
            for relationship in serialized_relationships
            if relationship.get("is_leading_candidate")
            and not relationship.get("ownership_pattern_signal")
        ),
        None,
    )
    if leading_candidate is None and serialized_relationships:
        leading_candidate = next(
            (
                relationship
                for relationship in serialized_relationships
                if not relationship.get("ownership_pattern_signal")
            ),
            None,
        )
    if (
        actual_controller is None
        and leading_candidate is not None
        and leading_candidate in serialized_relationships
    ):
        leading_candidate["is_leading_candidate"] = True
        leading_candidate["controller_status"] = (
            leading_candidate.get("controller_status") or CONTROLLER_STATUS_LEADING
        )
        leading_candidate["selection_reason"] = (
            leading_candidate.get("selection_reason") or "leading_candidate"
        )
        if isinstance(leading_candidate.get("basis"), dict):
            leading_candidate["basis"]["is_leading_candidate"] = True
            leading_candidate["basis"]["controller_status"] = (
                leading_candidate["basis"].get("controller_status")
                or CONTROLLER_STATUS_LEADING
            )
            leading_candidate["basis"]["selection_reason"] = (
                leading_candidate["basis"].get("selection_reason")
                or "leading_candidate"
            )

    country_attribution = (
        db.query(CountryAttribution)
        .filter(CountryAttribution.company_id == company_id)
        .filter(
            or_(
                CountryAttribution.notes.is_(None),
                ~CountryAttribution.notes.like("MANUAL_OVERRIDE:%"),
            )
        )
        .order_by(CountryAttribution.id.desc())
        .first()
    )
    country_basis = _normalize_country_basis_payload(
        country_attribution.basis if country_attribution is not None else None,
        attribution_type=(
            _canonical_attribution_type(country_attribution.attribution_type)
            if country_attribution is not None
            else None
        ),
    )

    if leading_candidate is None and isinstance(country_basis, dict):
        basis_leading_candidate = country_basis.get("leading_candidate")
        if isinstance(basis_leading_candidate, dict) and not bool(
            basis_leading_candidate.get("ownership_pattern_signal")
        ):
            leading_candidate = {
                "id": 0,
                "company_id": company_id,
                "controller_entity_id": basis_leading_candidate.get(
                    "controller_entity_id"
                ),
                "controller_name": basis_leading_candidate.get("controller_name")
                or "Unknown controller candidate",
                "controller_type": basis_leading_candidate.get("controller_type")
                or "other",
                "control_type": basis_leading_candidate.get("control_type")
                or basis_leading_candidate.get("classification")
                or "significant_influence",
                "control_ratio": basis_leading_candidate.get("control_ratio")
                or basis_leading_candidate.get("total_score_pct"),
                "control_path": basis_leading_candidate.get("control_path")
                or country_basis.get("top_paths")
                or [],
                "is_actual_controller": False,
                "whether_actual_controller": False,
                "basis": basis_leading_candidate,
                "notes": None,
                "control_mode": basis_leading_candidate.get("control_mode"),
                "semantic_flags": basis_leading_candidate.get("semantic_flags"),
                "controller_status": basis_leading_candidate.get("controller_status")
                or country_basis.get("controller_status"),
                "selection_reason": basis_leading_candidate.get("selection_reason"),
                "is_leading_candidate": True,
                "review_status": None,
                "created_at": "",
                "updated_at": "",
            }

    identification_status = CONTROLLER_STATUS_NONE
    if actual_controller is not None:
        identification_status = CONTROLLER_STATUS_ACTUAL
    elif leading_candidate is not None:
        identification_status = (
            leading_candidate.get("controller_status") or CONTROLLER_STATUS_LEADING
        )
    elif isinstance(country_basis, dict) and country_basis.get("controller_status"):
        identification_status = country_basis["controller_status"]

    display_controller = actual_controller or leading_candidate
    display_controller_role = (
        "actual_controller"
        if actual_controller is not None
        else "leading_candidate" if leading_candidate is not None else None
    )

    return {
        "company_id": company_id,
        "controller_count": len(relationships),
        "control_relationships": serialized_relationships,
        "direct_controller": direct_controller,
        "actual_controller": actual_controller,
        "leading_candidate": leading_candidate,
        "focused_candidate": leading_candidate,
        "display_controller": display_controller,
        "display_controller_role": display_controller_role,
        "identification_status": identification_status,
        "controller_status": identification_status,
    }


